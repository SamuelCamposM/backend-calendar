import { Schema, model } from "mongoose";

const UsuarioSchema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export const UsuarioModel = model("Usuario", UsuarioSchema);
